/**
 * Creating a sidebar enables you to:
 - create an ordered group of docs
 - render a sidebar for each doc of that group
 - provide next/previous navigation

 The sidebars can be generated from the filesystem, or explicitly defined here.

 Create as many sidebars as you want.
 */
async function getDocsLinks() {
  // read all documents at directory
  var fs = require('fs');
  var path = require('path');
  var filesPath = path.join(__dirname, 'docs', 'reference', 'classes');

  // please note: links are pointing to /docs/reference, without versioning
  // as the path has changed, links are not correctly resolving for `next` version
  const files = await fs.readdirSync(filesPath);
  let items = [];

  // add global
  items.push({
    type: 'doc',
    label: 'Index',
    id: 'reference/index',
    customProps: {
      fragment: '',
      label: 'Index',
    },
  });

  // add functions
  items.push({
    type: 'doc',
    label: 'iasql_functions',
    id: 'reference/index',
    customProps: {
      fragment: 'iasql_functions',
      label: 'iasql_functions',
    },
  });

  for (const file of files) {
    // identify the ones with Module.md
    if (file.endsWith('Module.md')) {
      // name is just the first bit
      const entries = file.split('.');
      if (entries.length == 3) {
        items.push({
          type: 'doc',
          label: entries[0],
          id: 'reference/index',
          customProps: {
            fragment: entries[0],
            label: entries[0],
          },
        });
      }
    }
  }
  return items;
}

module.exports = (async function () {
  // By default, Docusaurus generates a sidebar from the docs folder structure
  const items = await getDocsLinks();
  return {
    docs: [
      'getting-started',
      {
        type: 'category',
        label: 'Sample Queries',
        collapsible: true,
        collapsed: true,
        items: [
          {
            type: 'autogenerated',
            dirName: 'sample-queries',
          },
        ],
      },
      {
        type: 'category',
        label: 'How-to guides',
        collapsible: true,
        collapsed: true,
        items: [
          {
            type: 'autogenerated',
            dirName: 'how-to',
          },
        ],
      },
      {
        type: 'category',
        label: 'Concepts',
        collapsible: true,
        collapsed: true,
        items: [
          {
            type: 'autogenerated',
            dirName: 'concepts',
          },
        ],
      },
      {
        type: 'category',
        label: 'Reference',
        collapsible: true,
        collapsed: true,
        items: items,
      },
    ],
  };
})();
